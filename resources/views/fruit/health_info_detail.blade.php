                                       <h2>Q3：我很健康，需要打流感疫苗嗎？</h2>
                                       <div class="text">
                                       <span>A：</span>
                                        <p>兩者並不相同。普通感冒的致病源頭是數百種不同的病毒；流感則是由流感病毒引起的急性呼吸道傳染病，引起的症狀、輕重也不盡相同。</p>
                                       </div>
