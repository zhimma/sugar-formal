                              <h2>不只武漢肺炎，流感肆虐中！</h2>
                              <h3>2020-03-08 作者 果珍的健康生活</h3>
                              <div class="feline"></div>
                              <div class="zxxq_1">
                               <p>新冠肺炎（武漢肺炎）蔓延全球，對全世界造成的疫情，讓各國都緊繃神經以對。事實上，除了新冠肺炎，流感威力卻也不容小覷。根據世界衛生組織的描述：「每年冬天，數千萬人感染流感，雖然多數人只會生病一星期而無法工作，但老年人則有更高的死亡風險。」對於流感的發生以及流感疫苗的注射，民眾有許多的疑慮：不就是「流行性」感冒，有這麼嚴重嗎？我該打疫苗嗎？…</p>
                               <img src="/fruit/images/imgx_01.png" class="mg-responsive center-block ">
                               <p>針對民眾可能有的疑問，我們以「Q & A」的方式一一為大家釋疑。</p>
                               <div id="wt">
                                       <h2>Q1：為何流感總是『大約在冬季』？</h2>
                                       <div class="text">
                                       <span>A：</span>
        <p>在臺灣，流感開始流行的季節大概是在每年入秋之後，從當年 11 月至隔年 2 月是高峰期，隔年 3 月才逐漸下降。可以這麼說，流感在冬天達到高峰。</p>
        <p>流感為什麼總是冬季爆發而不是像感冒一樣全年都有？對科學家而言，這也是未解之謎。科學家總是心存疑問而提出許多「假說」，而假說需要經過「證明」才能成為「事實」。到目前為止，假說仍然在假說階段。
        一個可能的假說是，人們冬天待在室內的時間比較長，互相接觸的機會也比較多，這會增加人與人之間傳染的機會；另外，冬季時，北半球在冬季有比較多的慶祝（感恩節、聖誕節），有很多的聚集活動（購物、家人聚會），更有利於流感傳播。</p>
        <p>另一個可能的假說是，冬天氣溫較低，空氣較乾燥，這會使呼吸道黏膜水分減少，使呼吸道無法有效排除病毒顆粒，病毒附著在黏膜的時間變長，更容易發病。另外，病毒在低溫的表面也能存活較久，乾冷的環境（小於 5°C）讓呼吸時呼出「水霧」，更利於病毒傳染。冬季較低的「相對濕度」有可能是流感傳播的主因。</p>
                                       </div>
                               </div>
