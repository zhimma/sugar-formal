                                        <p>民眾可能認為自己身強體壯，不會得流感，並不需要打流感疫苗。這也沒錯。但是流感是一種非常容易傳播的疾病，疾病管制署會建議民眾打流感疫苗的原因有</p>
                                        <h4>2、群體免疫</h4>
                                        <p>當患者 A 打噴嚏，病毒就會向四周的人群（C、D、E、F、G）傳播。如果 C、D、E、F、G 全都沒有接種流感疫苗，這 5 個人都有機會得流感，也有機會繼續向其他人傳播流感（5 個人，5 條線）。如果其中 C、D、E 三人有打流感疫苗，從 A 患者處得流感的機會便會減少，這三人繼續傳播流感機率也大大減少（3 人免疫，病毒只剩 2 條線傳播）。</p>
