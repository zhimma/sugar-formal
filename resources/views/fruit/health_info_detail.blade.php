                                       <h2>Q2：流感和普通感冒哪裡不同？—兩者差很大</h2>
                                       <div class="text">
                                       <span>A：</span>
                                        <p>民眾可能認為自己身強體壯，不會得流感，並不需要打流感疫苗。這也沒錯。但是流感是一種非常容易傳播的疾病，疾病管制署會建議民眾打流感疫苗的原因有 2 個：</p>
